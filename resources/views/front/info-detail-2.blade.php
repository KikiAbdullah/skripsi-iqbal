                        <h3 class="post-title">1. Pastikan motor selalu dipanasi di pagi hari</h3>
                        <p style="text-align: justify; ">Mengendarai motor yang belum di panasi sama aja dengan kamu
                            membunuh motormu secara perlahan. Dan membunuh itu tidak dianjurkan oleh agama maupun
